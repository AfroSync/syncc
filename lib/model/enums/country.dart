  // Search functionality
  static List<Country> search(String query) {
    if (query.isEmpty) return Country.values;

    final lowerQuery = query.toLowerCase();
    return Country.values
        .where(
          (country) =>
              country.name.toLowerCase().startsWith(lowerQuery) ||
              country.shortCode.toLowerCase().startsWith(lowerQuery) ||
              country.phoneCode.startsWith(query),
        )
        .toList();
  }

  // Get country by short code
  static Country? fromShortCode(String code) {
    try {
      return Country.values.firstWhere(
        (country) => country.shortCode.toLowerCase() == code.toLowerCase(),
      );
    } catch (e) {
      return null;
    }
  }

  // Get country by phone code
  static Country? fromPhoneCode(String code) {
    try {
      return Country.values.firstWhere((country) => country.phoneCode == code);
    } catch (e) {
      return null;
    }
  }
